    public boolean verifyFields(){
        String fname = jTextField_fn.getText();
        String lname = jTextField_ln.getText();
        String stID = jTextField_sid.getText();
        String dept = jTextField_dpt.getText();
        String contNo = jTextField_cno.getText();
        String uname = userName.getText();
        String pass = String.valueOf(jPasswordField1.getPassword());
        if(fname.trim().equals("") || lname.trim().equals("") || stID.trim().equals("") || dept.trim().equals("") || contNo.trim().equals("") || uname.trim().equals("") || pass.trim().equals("")){
            JOptionPane.showMessageDialog(null, "One or more fields are empty", "Empty Fields", 2);
            return false;
        }
        else
            return true;
    }
    public boolean checkUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist = false;
        String query = "SELECT * FROM INFO WHERE U_NAME = ?";
        try {
            st = con.prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            if(rs.next()){
                username_exist = true;
                JOptionPane.showMessageDialog(null, "User Name Already Taken", "Username Failed", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(SignupForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exist;
    }
    public boolean validUsername(String input){
        String mailreg = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$";
        Pattern mailpat = Pattern.compile(mailreg, Pattern.CASE_INSENSITIVE);
        Matcher mat = mailpat.matcher(input);
        return mat.find();
    }
