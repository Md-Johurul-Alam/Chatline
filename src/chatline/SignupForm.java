    private void cbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbutActionPerformed
        String fname = jTextField_fn.getText();
        String lname = jTextField_ln.getText();
        String stID = jTextField_sid.getText();
        String dept = jTextField_dpt.getText();
        String contNo = jTextField_cno.getText();
        String uname = userName.getText();
        String pass = String.valueOf(jPasswordField1.getPassword());
        if(verifyFields()){
            if(!checkUsername(uname)){
                PreparedStatement ps;
                ResultSet rs;
                String regUserQuery = "INSERT INTO INFO(FIRST_NAME, LAST_NAME, STD_ID, DEPT, CONT_NO, U_NAME, PASSWORD) VALUES (?,?,?,?,?,?,?)";
                if(!validUsername(uname)){
                    JOptionPane.showMessageDialog(null, "Invalid Email!");
                }else{
                    try {
                        ps = con.prepareStatement(regUserQuery);
                        ps.setString(1, fname);
                        ps.setString(2, lname);
                        ps.setString(3, stID);
                        ps.setString(4, dept);
                        ps.setString(5, contNo);
                        ps.setString(6, uname);
                        ps.setString(7, pass);
                        if(ps.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(null, "Your account has been created");
                            jTextField_fn.setText("");
                            jTextField_ln.setText("");
                            jTextField_sid.setText("");
                            jTextField_dpt.setText("");
                            jTextField_cno.setText("");
                            userName.setText("");
                            jPasswordField1.setText("");
                            
                        }else{
                            JOptionPane.showMessageDialog(null, "Error! Check your informations");
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(SignupForm.class.getName()).log(Level.SEVERE, null, ex);
                    }  
                }
         

            }
        }
    }//GEN-LAST:event_cbutActionPerformed
    
